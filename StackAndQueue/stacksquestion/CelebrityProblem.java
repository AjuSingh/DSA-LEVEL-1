package StackAndQueue.stacksquestion;
import java.util.*;
public class CelebrityProblem {
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
		int n = sc.nextInt();
		 int[][] arr = new int[n][n];
	        for (int j = 0; j < n; j++) {
	            for (int k = 0; k < n; k++) {
	                arr[j][k] = sc.nextInt();
	            }
	        }
	}

}
